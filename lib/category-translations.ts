/**
 * Traduzir categoria do inglês para português
 */
export function translateCategory(englishCategory: string): string {
  return CATEGORY_TRANSLATIONS[englishCategory] || englishCategory
}

/**
 * Obter categoria em inglês a partir do português
 */
export function getCategoryInEnglish(portugueseCategory: string): string {
  const entry = Object.entries(CATEGORY_TRANSLATIONS).find(([en, pt]) => pt === portugueseCategory)
  return entry ? entry[0] : portugueseCategory
}

/**
 * Detectar idioma da categoria
 */
export function detectCategoryLanguage(category: string): 'en' | 'pt' {
  if (CATEGORY_TRANSLATIONS[category]) {
    return 'en'
  }
  
  const isPortuguese = Object.values(CATEGORY_TRANSLATIONS).includes(category)
  return isPortuguese ? 'pt' : 'en'
}

/**
 * Obter ambas as versões da categoria
 */
export function getBothCategoryVersions(category: string): { en: string; pt: string } {
  const language = detectCategoryLanguage(category)
  
  if (language === 'en') {
    return {
      en: category,
      pt: translateCategory(category)
    }
  } else {
    return {
      en: getCategoryInEnglish(category),
      pt: category
    }
  }
} 